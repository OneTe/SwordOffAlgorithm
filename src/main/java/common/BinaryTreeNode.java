public class BinaryTreeNode {
    int val;
    public BinaryTreeNode left;
    public BinaryTreeNode right;
    public BinaryTreeNode parent;
    public BinaryTreeNode(int val){
        this.val=val;
        this.left=left;
        this.right=right;
        this.parent=parent;
    }
}
